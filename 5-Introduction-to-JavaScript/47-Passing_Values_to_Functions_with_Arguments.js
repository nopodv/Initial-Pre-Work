// Passing values to functions with arguments

/* Parameters are added to functions when they are defined. Those parameters
are then assigned values when the function is called. They are called
arguments at that point. */
// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

// Only change code below this line.
function functionWithArgs(a, b) {
  console.log(a + b);
}
functionWithArgs(10, 50);
