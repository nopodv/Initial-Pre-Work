// Iterate odd numbers with a for loop

/* Creates a loop to to add odd numbers to an array "for" a specified number of
times. Then it will stop. Being sure to change the final expression is what
will allow the result to increment how you like. */
// Example
var ourArray = [];

for (var i = 0; i < 10; i += 2) {
  ourArray.push(i);
}

